package kr.co.ezenac.dto;

public class MainboardboxVO {
	private int mainboard_idx;  
	private String mainboard_name;
	private String mainboard_image;
	private int mainboard_price;
	
	public int getMainboard_idx() {
		return mainboard_idx;
	}
	public void setMainboard_idx(int mainboard_idx) {
		this.mainboard_idx = mainboard_idx;
	}
	public String getMainboard_name() {
		return mainboard_name;
	}
	public void setMainboard_name(String mainboard_name) {
		this.mainboard_name = mainboard_name;
	}
	public String getMainboard_image() {
		return mainboard_image;
	}
	public void setMainboard_image(String mainboard_image) {
		this.mainboard_image = mainboard_image;
	}
	public int getMainboard_price() {
		return mainboard_price;
	}
	public void setMainboard_price(int mainboard_price) {
		this.mainboard_price = mainboard_price;
	}
	
	
}
